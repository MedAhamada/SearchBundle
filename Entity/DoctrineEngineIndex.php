namespace MIT\Bundle\SearchBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * DoctrineEngineIndex
 *
 * @ORM\Table(name="doctrine_engine_index")
 * @ORM\Entity(repositoryClass="MIT\Bundle\SearchBundle\Repository\DoctrineEngineIndexRepository")
 */
class DoctrineEngineIndex
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="className", type="string", length=255)
     */
    private $className;

    /**
     * @var string
     *
     * @ORM\Column(name="slug", type="string", length=255, unique=true)
     */
    private $slug;

    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set className
     *
     * @param string $className
     *
     * @return DoctrineEngineIndex
     */
    public function setClassName($className)
    {
        $this->className = $className;

        return $this;
    }

    /**
     * Get className
     *
     * @return string
     */
    public function getClassName()
    {
        return $this->className;
    }

    /**
     * Set slug
     *
     * @param string $slug
     *
     * @return DoctrineEngineIndex
     */
    public function setSlug($slug)
    {
        $this->slug = $slug;

        return $this;
    }

    /**
     * Get slug
     *
     * @return string
     */
    public function getSlug()
    {
        return $this->slug;
    }
}

